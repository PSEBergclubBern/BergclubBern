namespace BergclubPlugin\Commands\Entities;


class Adressen
{
    public $id;
    public $firstName;
    public $lastName;
    public $salutation;
    public $number;
    public $category;
    public $street;
    public $plz;
    public $place;
    public $phonePrivate;
    public $phoneBusiness;
    public $phoneMobile;
    public $email;
    public $birthday;
    public $ahv;

    public function __toString()
    {
        return 'ID: ' . $this->id . '(' . $this->firstName . '/' . $this->lastName . ')';
    }
}